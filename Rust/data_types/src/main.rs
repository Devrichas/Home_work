fn main(){
    //          vvv "i8/16/32/64 & 128 " integer data type, which can hold up to 128 bits of data.
    let number: i32 = 23;

    //         vvv "f32/64" floating point data type, which can hold up to 64 bits of data.
    let float: f32 = 3.14;

    //        vvv "bool" boolean data type, which can hold either true or false values.
    let boolean: bool = true;

    //        vvv "char" character data type, which can hold a single Unicode character.
    let character: char = 'R';

    println!("Integer: {}", number);
    println!("Float: {}", float);
    println!("Boolean: {}", boolean);
    println!("Character: {}", character);

}