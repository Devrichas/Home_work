fn main() {
    let number1 = 10;
    let number2 = 32;

    if number1 > number2 {
        println!("{} > {}", number1, number2);
    }
    if number1 < number2 {
        println!("{} < {}", number1, number2);
    }
    else if number1 == number2 {
        println!("{} == {}", number1, number2);
    }
}
